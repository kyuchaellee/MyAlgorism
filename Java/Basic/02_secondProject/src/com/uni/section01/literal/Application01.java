package com.uni.section01.literal;
/*패키지 선언부
 * 가장 첫번째줄에 작성해야 한다.
 * 모든 클래스는 패키지 내에 존재해야 한다.
 * com.uni.section01 등
 * section.uni.com*/

public class Application01 {

	public static void main(String[] args) {
		/* 여러 가지 값의 형태를 출력해보자 */
	      /* 1. 숫자 형태의 값
	       *  1-1. 정수 형태의 값
	       *  1-2. 실수 형태의 값
	       * 2. 문자 형태의 값
	       * 3. 문자열 형태의 값
	       * 4. 논리 형태의 값
	       */
	      
	      /* 1. 숫자 형태의 값 */
	      /* 1-1. 정수 형태의 값 출력 */
	      System.out.println(123);
	      /* 1-2. 실수 형태의 값 출력 */
	      System.out.println(1.23);
	      
	      /* 2. 문자 형태의 값 출력 */
	      System.out.println('a');         
	      System.out.println("ab");         
	      //System.out.println('');         
	      System.out.println('1');         
	      
	      /* 3. 문자열 형태의 값 출력 */
	      System.out.println("안녕하세요");      
	      System.out.println("123");         
	      System.out.println("");            
	      System.out.println("a");         
	      
	      /* 4. 논리 형태의 값 출력 */
	      System.out.println(true);
	      System.out.println(false);
	}

}
