# 아래의 상품 딕셔너리 데이터를 가격에 따라 내림차순으로 정렬하는 프로그램을 작성하십시오.
 
# "TV": 2000000,

# "냉장고": 1500000,

# "책상": 350000,

# "노트북": 1200000,

# "가스레인지": 200000,

# "세탁기": 1000000

product = {"TV": 2000000, "냉장고": 1500000, "책상": 350000, "노트북": 1200000, "가스레인지": 200000, "세탁기": 1000000}

result = sorted(product.items(),key=lambda x : x[1],reverse=True)

for key,value in result:
    print("{0}: {1}".format(key,value))